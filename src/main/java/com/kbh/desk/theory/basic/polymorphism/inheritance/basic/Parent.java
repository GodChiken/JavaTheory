package com.kbh.desk.theory.basic.polymorphism.inheritance.basic;

public class Parent {
    private String name;
    private int age;

    public Parent(String name, int age){
        this.name = name;
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }
}
